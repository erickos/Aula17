/**
 * Abstract class to Forma as a geometric generic form.
 *
 * @author     Erick de Oliveira Silva
 * @version    2017.09.23
 */
abstract public class Forma
{

	/**
	 * Calculates the area of the form.
	 *
	 * @return     The form area as float value.
	 */
	abstract public float calcularArea();

	/**
	 * Calculates de perimeter of the form.
	 *
	 * @return     The form perimeter as float value.
	 */
	abstract public float calcularPerimetro();
}